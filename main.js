//css
function updateProgressBarCss(percentage) {
    const progressBar = document.querySelector('.progress-css');
    progressBar.style.width = percentage + '%';
  
    const percentageText = document.querySelector('.percentage-css');
    percentageText.textContent = percentage + '%';
  }
  
  // Chamando a função com o valor desejado (exemplo: 70%)
  updateProgressBarCss(85);

